#!/usr/bin/env node
const assert = require('assert');
const { createSession, ensureActiveSession, isSessionBusy } = require('../../client/app/state');

const session = createSession('T', () => 123456, () => 0.123456);
assert.ok(session.id.startsWith('chat-'));
assert.strictEqual(session.title, 'T');
assert.deepStrictEqual(session.messages, []);
assert.strictEqual(session.imageStylePrompt, '');
assert.strictEqual(session.hasImageStylePromptOverride, false);
assert.strictEqual(session.chatModel, '');
const state = { sessions: [], activeSessionId: '', busySessions: new Set() };
const active = ensureActiveSession(state, () => ({ ...session, id: 's1' }));
assert.strictEqual(active.id, 's1');
assert.strictEqual(state.activeSessionId, 's1');
state.sessions[0].headerValues = undefined;
state.sessions[0].hasSystemPromptOverride = undefined;
state.sessions[0].hasImageStylePromptOverride = undefined;
ensureActiveSession(state);
assert.deepStrictEqual(state.sessions[0].headerValues, {});
assert.strictEqual(state.sessions[0].systemPrompt, '');
assert.strictEqual(state.sessions[0].imageStylePrompt, '');
assert.strictEqual(state.sessions[0].chatModel, '');
assert.strictEqual(state.sessions[0].hasSystemPromptOverride, false);
assert.strictEqual(state.sessions[0].hasImageStylePromptOverride, false);
state.busySessions.add('s1');
assert.strictEqual(isSessionBusy(state, 's1'), true);
state.busySessions.clear();
state.sessions[0].busy = true;
assert.strictEqual(isSessionBusy(state, 's1'), true);
console.log('app state ok');
